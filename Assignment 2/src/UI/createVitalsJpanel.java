    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        
        if(!isDouble(txtBodyTemp.getText())) {
            JOptionPane.showMessageDialog(this, "Patient Body Temperature is not a double");
            return;
        } 
        
        double bodyTemperature = Double.parseDouble(txtBodyTemp.getText());
        if(!isDouble(txtBP.getText())) {
            JOptionPane.showMessageDialog(this, "Patient Blood Pressure is not a double");
            return;
        }
        double bloodPressure = Double.parseDouble(txtBP.getText());
        if(!isNumeric(txtPulse.getText())) {
            JOptionPane.showMessageDialog(this, "Patient Pulse is not number");
            return;
        }
       
        int pulse = Integer.parseInt(txtPulse.getText());
        if(!isNumeric(txtAge.getText())) {
            JOptionPane.showMessageDialog(this, "Patient Age is not number");
            return;
        }
        
            
        int age = Integer.parseInt(txtAge.getText());
        if(!isNumeric(txtRR.getText())) {
            JOptionPane.showMessageDialog(this, "Patient Respiraation Rate is not number");
            return;
        }
        int respirationRate = Integer.parseInt(txtRR.getText());
        String date = this.jDateChooser1.getDate().toString();
        
        
        
        VitalSigns vs = vital.addNewVitals();
        
        vs.setAge(age);
        vs.setBloodPressure(bloodPressure);
        vs.setPatientId(patientId);
        vs.setDate(date);
        vs.setBodyTemperature(bodyTemperature);
        vs.setRespirationRate(respirationRate);
        vs.setPulse(pulse);
        
        
        JOptionPane.showMessageDialog(this,"New Vital Signs Added");
        
        txtBodyTemp.setText("");
        txtBP.setText("");
        txtPulse.setText("");
        txtAge.setText("");
        txtRR.setText("");
         
        
        


          // TODO add your handling code here:
    }//GEN-LAST:event_btnSaveActionPerformed

